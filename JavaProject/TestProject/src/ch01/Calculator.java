package ch01;

public class Calculator {

	public void add(int num, int num1) {
		System.out.println(num + num1);
	}
	public void minus(int num, int num1) {
		System.out.println(num - num1);
	}
	public void multi(int num, int num1) {
		System.out.println(num * num1);
	}
	public void division(double num, double num1) {
		System.out.println(num / num1);
	}
}
